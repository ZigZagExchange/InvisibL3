function padSwapInputs(n) {
  const newInputs = {};
  for (const [key, value] of Object.entries(swapInputs)) {
    if (
      key == "notesIn_A" ||
      key == "notesOut_A" ||
      key == "notesIn_B" ||
      key == "notesOut_B"
    ) {
      newInputs[key] = padArrayEnd(value, n, [0n, 0n, 1n, 0n, 0n, 1n]);
    } else if (key == "pseudoComms_A" || key == "pseudoComms_B") {
      newInputs[key] = padArrayEnd(value, n, [0n, 1n]);
    } else if (
      key == "amountsIn_A" ||
      key == "amountsOut_A" ||
      key == "blindingsIn_A" ||
      key == "blindingsOut_A" ||
      key == "pos_A" ||
      key == "amountsIn_B" ||
      key == "amountsOut_B" ||
      key == "blindingsIn_B" ||
      key == "blindingsOut_B" ||
      key == "pos_B"
    ) {
      newInputs[key] = padArrayEnd(value, n, 0);
    } else if (key == "signature_A" || key == "signature_B") {
      newInputs[key] = padArrayEnd(value, n + 1, 0);
    } else {
      newInputs[key] = value;
    }
  }
  return newInputs;
}

function multiSwapInputs(numSwaps, n) {
  let inputs = padSwapInputs(n);

  const newInputs = {};
  for (const [key, value] of Object.entries(inputs)) {
    newInputs[key] = new Array(numSwaps).fill(value);
  }
  return newInputs;
}

function padArrayEnd(arr, len, padding) {
  return arr.concat(Array(len - arr.length).fill(padding));
}

